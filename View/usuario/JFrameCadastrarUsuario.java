    private void jButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarActionPerformed

        int opcao = controllerUsuario.confirmaInformacoes();
        if (opcao == 0) {
            Usuario usuario = controllerUsuario.getUsuario();
            usuario.setDataNascimento(jTextFieldNasc.getText());
            usuario.setEmail(jTextFieldEmail.getText());
            usuario.setNome(jTextFieldNome.getText());
            usuario.setNomePai(jTextFieldPai.getText());
            usuario.setNomeMae(jTextFieldMae.getText());
            usuario.setRg(jTextFieldRG.getText());
            usuario.setSexo(jTextFieldSexo.getText());
            controllerUsuario.setUsuario(usuario);
            if (endJaCadastrado) {
                controllerUsuario.alterar(this);
            } else {
                controllerEndereco.setControllerUsuario(controllerUsuario);
                controllerEndereco.novoCadastro(this);
                endJaCadastrado = true;
            }
        }

    }//GEN-LAST:event_jButtonCadastrarActionPerformed


    public void setParametro() {
        jTextFieldNome.setText(getControllerInstituicaoEnsino().getInstituicaoEnsino().getNome());
        jTextFieldLocal.setText(getControllerInstituicaoEnsino().getInstituicaoEnsino().getLocal());
        if (getControllerInstituicaoEnsino().getInstituicaoEnsino().isTipoMedio()) {
            jRadioButtonMedio.setSelected(true);
        }
        if (getControllerInstituicaoEnsino().getInstituicaoEnsino().isTipoTecnico()) {
            jRadioButtonTecnico.setSelected(true);
        }
        if (getControllerInstituicaoEnsino().getInstituicaoEnsino().isTipoSuperior()) {
            jRadioButtonSuperior.setSelected(true);
        }
        if (getControllerInstituicaoEnsino().getInstituicaoEnsino().isTipoMestrado()) {
            jRadioButtonMestrado.setSelected(true);
        }
        if (getControllerInstituicaoEnsino().getInstituicaoEnsino().isTipoDoutorado()) {
            jRadioButtonDoutorado.setSelected(true);
        }
        if (getControllerInstituicaoEnsino().getInstituicaoEnsino().isTipoPhd()) {
            jRadioButtonPHD.setSelected(true);
        }
    }


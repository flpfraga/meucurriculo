    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        int opcao = controllerInstituicaoEnsino.confirmaInformacoes();
        if (opcao == 0) {
            getControllerInstituicaoEnsino().getInstituicaoEnsino().setNome(jTextFieldNome.getText());
            getControllerInstituicaoEnsino().getInstituicaoEnsino().setLocal(jTextFieldNome.getText());
            getControllerInstituicaoEnsino().getInstituicaoEnsino().setTipoMedio(jRadioButtonMedio.isSelected());
            getControllerInstituicaoEnsino().getInstituicaoEnsino().setTipoTecnico(jRadioButtonTecnico.isSelected());
            getControllerInstituicaoEnsino().getInstituicaoEnsino().setTipoSuperior(jRadioButtonSuperior.isSelected());
            getControllerInstituicaoEnsino().getInstituicaoEnsino().setTipoMestrado(jRadioButtonMestrado.isSelected());
            getControllerInstituicaoEnsino().getInstituicaoEnsino().setTipoDoutorado(jRadioButtonDoutorado.isSelected());
            getControllerInstituicaoEnsino().getInstituicaoEnsino().setTipoPhd(jRadioButtonPHD.isSelected());
            controllerInstituicaoEnsino.alterar(this);
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

